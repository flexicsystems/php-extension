if (!\function_exists('is_uuid')) {
    function is_uuid(string $uuid): bool
    {
        return \preg_match('/^[0-9A-F]{8}-[0-9A-F]{4}-4[0-9A-F]{3}-[89AB][0-9A-F]{3}-[0-9A-F]{12}$/i', $uuid) === 1;
    }
}
